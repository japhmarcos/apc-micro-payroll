<h1>About</h1>

<div style="text-align: justify;">
In 1979, Mr. Eugenio J. Vinta, the company’s chairman, established Micromatic Industries Incorporated which then was mainly focused on the business of assembly and retail sale of electrical products and services. In the year 1989, the company widened its range by venturing into manufacturing and started fabricating metal enclosures, cable trays, wire rays, and other allied products. They began offering electrical installation on a selective basis in 1994, providing their clients with high-quality products and services. During the same year, the company was registered with the Securities and Exchange Commission under its current name.
With the recent market trends, the management decided to establish a separate entity for its retail and trading business which paved the way for their affiliate, Team Options Mercantile Incorporated (TOMI) in 2003. TOMI was intended to handle the retail and trading operations side of the business while the main company focused on its core business—manufacturing and electrical installation. 2007 was the year when they started importing raw materials to lower production cost and new products which were not readily available in their market.
Micromatic Industries’ multitude of product lines are but not limited to the following:<br><br>
